def km_to_miles():
    km = float(input('Enter Kilometers to convert into Miles: '))
    miles = km * 0.62137
    print(f'{km} km in to Miles are: {miles}')
km_to_miles()

def mile_to_km():
    miles = float(input('Enter Miles to convert into KiloMeter: '))
    km = miles * 1.609
    print(f'{miles} Miles in to Miles are: {km} KiloMeters')
mile_to_km()